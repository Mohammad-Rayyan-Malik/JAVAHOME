class bitwise// its deal with binary no.
					// images ke time per binary me deal kiye jata hai other wise decimal me hi hum mostly deal karte hain
{
	public static void main(String[]args)
	
	{
	//bitwise & operator
	System.out.println(100&8);//1100100
							  //0001000
							  // 0000000
							  
	// bitwise | operator
	System.out.println(100|8);//1100100
							  //0001000
							  //1101100
	System.out.println(100|8);//1100100
							  //0001000						  
							  
	// bitwise xor
		System.out.println(100^9);//1100100
								  //0001001	
								  //1101101
							

	// bitwise !
		System.out.println(100);//0000000001100100
								  //1111111110011011	
							  
							  
	}

}