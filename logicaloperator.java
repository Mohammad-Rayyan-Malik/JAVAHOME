class logicaloperator
{
	public static void main(String[]args)
	{	
	
		int Rayyan=10;
		int Asrar=12;
		
		// and (&&) operator
		System.out.println(Rayyan>Asrar && Asrar!=Rayyan);
		System.out.println(Rayyan<Asrar && Asrar!=Rayyan);
		
		// or (||) operator
		System.out.println(Rayyan>1000  ||   Asrar==12);
		
		
		// ! NOt operator
		System.out.println(!(Rayyan>Asrar && Asrar!=Rayyan));
		
		
		
		


		}
}