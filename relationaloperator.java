
class relationaloperator
{
	public static void main (String[]args)
	{
	// relationaloperator give the only single value like true and false
	int Rayyan=100;
	int Asrar=200;
	
	System.out.println(Rayyan==Asrar);
	System.out.println(Rayyan=Asrar);
	System.out.println(Rayyan);
	System.out.println(Rayyan<Asrar);
	System.out.println(Rayyan>Asrar);	
	System.out.println(Rayyan==Asrar);
	System.out.println(Rayyan!=Asrar);
	}

}